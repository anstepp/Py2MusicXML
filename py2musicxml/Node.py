import note.py

class Node:
	location = None
	level = None
	children = []
	parent = []

	# I don't think we should allow init that isn't
	# either Meter or Phrase
	def __init__(self):
		if len(parent) > 1:
			print("Too many parents")

	def getParent(self):
		return parent

	def getSiblings(self):
		return parent.children

	def getChildren(self):
		return children

